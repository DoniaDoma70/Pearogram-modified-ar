const languageSelector = document.querySelector("select");

languageSelector.addEventListener("change", (Event) => {
    setLanguage(Event.target.value);
    localStorage.setItem("language", Event.target.value);
});

document.addEventListener("DOMContentLoaded", () => {
    setLanguage(localStorage.getItem("language"));
});


const setLanguage = (lang) => {
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach((element) => {
        const key = element.getAttribute("data-i18n");
        element.textContent = translation[lang][key];
    }); 
    if (lang == "ar") {
        document.dir = "rtl";
        document.getElementById("headWelcomeSpeech").style.letterSpacing = "0";
        document.getElementById("welcomeSpeechPara").style.letterSpacing = "0";
        $("head").append('<link id="bootstrapRTL" rel="stylesheet" href="https://cdn.rtlcss.com/bootstrap/3.3.7/css/bootstrap.min.css">');
        $("head").append('<link id="translaionCSS" rel="stylesheet" href="assets/css/translation.css">'); 
        //$("body").append(content);
    } else {
        document.dir = "ltr";
        document.getElementById("headWelcomeSpeech").style.letterSpacing = "6px";
        document.getElementById("welcomeSpeechPara").style.letterSpacing = "3px";
        $("#bootstrapRTL").remove();
        $("#translaionCSS").remove();
    }
    //document.dir = lang === "ar" ? "rtl" : "ltr";
};


